/**
 * generate-icon-registry.mjs
 *
 * Reads the reviewed community icon SVGs under icons/community/ and generates a
 * typed TypeScript registry with each SVG inlined as a string, so the icon
 * component renders complete markup in the initial server response with no
 * runtime fetch. Adapted from the v20 registry generator (immutable commit
 * 02880fbc995b45d459ce4f264b29d5283b1d8ced, scripts/generate-icon-registry.ts).
 *
 * The proprietary tier keeps its own generated registry inside the
 * @myrrys/proprietary submodule; this generator never touches proprietary
 * artwork, keeping the licensing boundary intact.
 *
 * Run:   node scripts/generate-icon-registry.mjs
 * Check: node scripts/generate-icon-registry.mjs --check
 */
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { basename, dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const svgDir = join(root, "icons/community");
const outFile = join(root, "icons/community.ts");

const HEADER = "// @generated by scripts/generate-icon-registry.mjs — DO NOT EDIT\n";

function generate() {
  const nouns = readdirSync(svgDir)
    .filter((f) => f.endsWith(".svg"))
    .map((f) => basename(f, ".svg"))
    .sort();

  const nounType = nouns.map((n) => `  | ${JSON.stringify(n)}`).join("\n");

  const registryEntries = nouns
    .map((noun) => {
      // Strip XML comments and trim so Illustrator timestamps do not create
      // spurious registry churn between exports of the same artwork.
      const svg = readFileSync(join(svgDir, `${noun}.svg`), "utf-8")
        .replace(/<!--[\s\S]*?-->/g, "")
        .trim();
      return `  ${JSON.stringify(noun)}: ${JSON.stringify(svg)}`;
    })
    .join(",\n");

  return `${HEADER}
export type CommunityNoun =
${nounType};

const _registry: Record<CommunityNoun, string> = {
${registryEntries},
};

export function getIcon(noun: CommunityNoun | (string & {})): string | undefined {
  return _registry[noun as CommunityNoun];
}

export function getNouns(): CommunityNoun[] {
  return Object.keys(_registry) as CommunityNoun[];
}
`;
}

const generated = generate();

if (process.argv.includes("--check")) {
  let existing = "";
  try {
    existing = readFileSync(outFile, "utf-8");
  } catch {
    // missing file is stale
  }
  if (existing !== generated) {
    console.error(`STALE: ${outFile}\nRun: pnpm --filter @pelilauta/design-system generate:icons`);
    process.exit(1);
  }
  console.log(`OK: ${outFile}`);
} else {
  writeFileSync(outFile, generated, "utf-8");
  console.log(`Generated: ${outFile}`);
}
