/**
 * generate-icon-registry.mjs
 *
 * Reads the reviewed community icon SVGs under icons/community/ and generates a
 * typed TypeScript registry. Normalization happens HERE, at generation time,
 * not at render time: each icon is stored as pre-extracted inner markup plus its
 * viewBox, so the icon component renders complete markup in the initial server
 * response with no runtime fetch and no request-time <svg> stripping. Inner
 * markup — per-element fills, <style> blocks, <g> wrappers — is preserved
 * verbatim so currentColor inheritance and any encoded colors both survive.
 * Adapted from the v20 registry generator (immutable commit
 * 02880fbc995b45d459ce4f264b29d5283b1d8ced, scripts/generate-icon-registry.ts).
 *
 * The proprietary tier keeps its own generated registry inside the
 * @myrrys/proprietary submodule; this generator never touches proprietary
 * artwork, keeping the licensing boundary intact.
 *
 * Run:   node scripts/generate-icon-registry.mjs
 * Check: node scripts/generate-icon-registry.mjs --check
 */
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { basename, dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const svgDir = join(root, "icons/community");
const outFile = join(root, "icons/community.ts");

const HEADER = "// @generated by scripts/generate-icon-registry.mjs — DO NOT EDIT\n";

/**
 * Extract the inner markup and viewBox from a raw SVG document, dropping the
 * XML prolog, comments, and the outer <svg> wrapper. Inner content is kept
 * verbatim so authored fills and <style> blocks are never rewritten.
 */
function extract(svgRaw, noun) {
  const svg = svgRaw.replace(/<!--[\s\S]*?-->/g, "");
  const open = svg.match(/<svg\b[^>]*>/i);
  if (!open) throw new Error(`${noun}: no <svg> root element`);
  const vb = open[0].match(/viewBox\s*=\s*["']([^"']+)["']/i);
  const viewBox = vb ? vb[1] : "0 0 128 128";
  const start = open.index + open[0].length;
  const end = svg.lastIndexOf("</svg>");
  const inner = svg.slice(start, end).trim();
  return { inner, viewBox };
}

function generate() {
  const nouns = readdirSync(svgDir)
    .filter((f) => f.endsWith(".svg"))
    .map((f) => basename(f, ".svg"))
    .sort();

  const nounType = nouns.map((n) => `  | ${JSON.stringify(n)}`).join("\n");

  const registryEntries = nouns
    .map((noun) => {
      const raw = readFileSync(join(svgDir, `${noun}.svg`), "utf-8");
      const { inner, viewBox } = extract(raw, noun);
      return `  ${JSON.stringify(noun)}: { viewBox: ${JSON.stringify(
        viewBox,
      )}, inner: ${JSON.stringify(inner)} }`;
    })
    .join(",\n");

  return `${HEADER}
export type CommunityNoun =
${nounType};

export interface IconEntry {
  inner: string;
  viewBox: string;
}

const _registry: Record<CommunityNoun, IconEntry> = {
${registryEntries},
};

export function getIcon(noun: CommunityNoun | (string & {})): IconEntry | undefined {
  return _registry[noun as CommunityNoun];
}

export function getNouns(): CommunityNoun[] {
  return Object.keys(_registry) as CommunityNoun[];
}
`;
}

const generated = generate();

if (process.argv.includes("--check")) {
  let existing = "";
  try {
    existing = readFileSync(outFile, "utf-8");
  } catch {
    // missing file is stale
  }
  if (existing !== generated) {
    console.error(`STALE: ${outFile}\nRun: pnpm --filter @pelilauta/design-system generate:icons`);
    process.exit(1);
  }
  console.log(`OK: ${outFile}`);
} else {
  writeFileSync(outFile, generated, "utf-8");
  console.log(`Generated: ${outFile}`);
}
